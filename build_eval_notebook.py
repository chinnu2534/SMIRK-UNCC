    "        fn()\n",
    "    t1.record()\n",
    "    torch.cuda.synchronize()\n",
    "    ms = t0.elapsed_time(t1) / reps\n",
    "    vram_mb = torch.cuda.max_memory_allocated() / 1e6\n",
    "    return ms, vram_mb\n",
    "\n",
    "rows = []\n",
    "\n",
    "# Encoder\n",
    "with torch.no_grad():\n",
    "    ms, vram = cuda_timer(lambda: encoder(bench_tensor))\n",
    "rows.append({'module': 'SmirkEncoder', 'latency_ms': ms, 'fps': 1000/ms, 'vram_mb': vram})\n",
    "\n",
    "# Cache params for downstream\n",
    "with torch.no_grad():\n",
    "    p = encoder(bench_tensor)\n",
    "\n",
    "# FLAME\n",
    "with torch.no_grad():\n",
    "    ms, vram = cuda_timer(lambda: flame(\n",
    "        shape_params=p['shape_params'],\n",
    "        expression_params=p['expression_params'],\n",
    "        pose_params=p['pose_params'],\n",
    "    ))\n",
    "rows.append({'module': 'FLAME', 'latency_ms': ms, 'fps': 1000/ms, 'vram_mb': vram})\n",
    "\n",
    "# Renderer\n",
    "with torch.no_grad():\n",
    "    verts, _, _ = flame(\n",
    "        shape_params=p['shape_params'],\n",
    "        expression_params=p['expression_params'],\n",
    "        pose_params=p['pose_params'],\n",
    "    )\n",
    "    ms, vram = cuda_timer(lambda: renderer(verts, flame.faces_tensor))\n",
    "rows.append({'module': 'Renderer', 'latency_ms': ms, 'fps': 1000/ms, 'vram_mb': vram})\n",
    "\n",
    "# End-to-end\n",
    "def e2e():\n",
    "    with torch.no_grad():\n",
    "        pp = encoder(bench_tensor)\n",
