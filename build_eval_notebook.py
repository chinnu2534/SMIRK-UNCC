CELL_E7 = code("cell-e7", [
    "# ── E7: SSIM / PSNR / LPIPS on face-masked region ────────────────────────\n",
    "import torch, lpips, numpy as np\n",
    "from skimage.metrics import structural_similarity as ssim_fn, peak_signal_noise_ratio as psnr_fn\n",
    "\n",
    "loss_fn_lpips = lpips.LPIPS(net='alex').to(DEVICE)\n",
    "\n",
    "fidelity_rows = []\n",
    "\n",
    "for path in image_paths:\n",
    "    name = pathlib.Path(path).stem\n",
    "    if name not in results:\n",
    "        continue\n",
    "\n",
    "    bgr = cv2.imread(path)\n",
    "    crop, _, _ = get_face_crop(bgr)\n",
    "    if crop is None:\n",
    "        continue\n",
    "\n",
    "    params = encode_image(crop)\n",
    "\n",
    "    with torch.no_grad():\n",
    "        verts, lmks3d, _ = flame(\n",
    "            shape_params=params.get('shape_params'),\n",
    "            expression_params=params.get('expression_params'),\n",
    "            pose_params=params.get('pose_params'),\n",
    "        )\n",
    "        render_out = renderer(verts, flame.faces_tensor)\n",
    "\n",
    "    # rendered image and mask (C,H,W) in [0,1]\n",
    "    rendered = render_out['images'][0].clamp(0, 1)       # (3,H,W)\n",
    "    mask     = render_out['alpha_images'][0]             # (1,H,W)\n",
    "\n",
    "    gt_tensor = torch.from_numpy(crop).permute(2,0,1).float() / 255.0  # (3,H,W)\n",
    "    gt_tensor = gt_tensor.to(DEVICE)\n",
    "\n",
    "    # Apply foreground mask\n",
    "    m = (mask > 0.1).float()\n",
    "    rendered_m = rendered * m\n",
    "    gt_m       = gt_tensor * m\n",
    "\n",
    "    # Convert to numpy HWC uint8 for skimage\n",
    "    def t2np(t):\n",
    "        return (t.permute(1,2,0).cpu().numpy() * 255).clip(0,255).astype(np.uint8)\n",
    "\n",
    "    rnd_np = t2np(rendered_m); gt_np = t2np(gt_m)\n",
    "\n",
    "    ssim_val  = ssim_fn(rnd_np, gt_np, channel_axis=2, data_range=255)\n",
    "    psnr_val  = psnr_fn(gt_np, rnd_np, data_range=255)\n",
    "\n",
    "    # LPIPS expects [-1,1] range\n",
    "    def to_lpips(t): return (t * 2.0 - 1.0).unsqueeze(0)  # (1,3,H,W)\n",
    "    with torch.no_grad():\n",
    "        lpips_val = float(loss_fn_lpips(to_lpips(rendered_m), to_lpips(gt_m)))\n",
    "\n",
    "    fidelity_rows.append({'name': name, 'ssim': ssim_val, 'psnr': psnr_val, 'lpips': lpips_val})\n",
    "    print(f'  {name:20s}  SSIM={ssim_val:.4f}  PSNR={psnr_val:.2f}dB  LPIPS={lpips_val:.4f}')\n",
    "\n",
    "df_fidelity = pd.DataFrame(fidelity_rows).set_index('name')\n",
    "print(f'\\nMean SSIM : {df_fidelity.ssim.mean():.4f} ± {df_fidelity.ssim.std():.4f}')\n",
    "print(f'Mean PSNR : {df_fidelity.psnr.mean():.2f} ± {df_fidelity.psnr.std():.2f} dB')\n",
    "print(f'Mean LPIPS: {df_fidelity.lpips.mean():.4f} ± {df_fidelity.lpips.std():.4f}')\n",
    "df_fidelity.to_csv('/content/smirk_eval_fidelity.csv')\n",
])

