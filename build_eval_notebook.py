    "\n",
    "bench_tensor = TRANSFORM(bench_crop).unsqueeze(0).to(DEVICE)\n",
    "\n",
    "def cuda_timer(fn, warmup=10, reps=50):\n",
    "    for _ in range(warmup):\n",
    "        fn()\n",
    "    torch.cuda.synchronize()\n",
    "    torch.cuda.reset_peak_memory_stats()\n",
    "    t0 = torch.cuda.Event(enable_timing=True)\n",
    "    t1 = torch.cuda.Event(enable_timing=True)\n",
