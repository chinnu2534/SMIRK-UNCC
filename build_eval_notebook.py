CELL_E5 = code("cell-e5", [
    "# ── E5: Helper functions — crop, encode, NME ─────────────────────────────\n",
    "import cv2, mediapipe as mp, numpy as np, torch\n",
    "from torchvision import transforms\n",
    "\n",
    "IMG_SIZE = 224\n",
    "TRANSFORM = transforms.Compose([\n",
    "    transforms.ToTensor(),\n",
    "    transforms.Normalize([0.5]*3, [0.5]*3),\n",
    "])\n",
    "\n",
    "# MediaPipe face detector\n",
    "BaseOptions = mp.tasks.BaseOptions\n",
    "FaceLandmarker = mp.tasks.vision.FaceLandmarker\n",
    "FaceLandmarkerOptions = mp.tasks.vision.FaceLandmarkerOptions\n",
    "VisionRunningMode = mp.tasks.vision.RunningMode\n",
    "\n",
    "MP_MODEL_PATH = '/content/smirk/mediapipe_models/face_landmarker_v2_with_blendshapes.task'\n",
    "mp_opts = FaceLandmarkerOptions(\n",
    "    base_options=BaseOptions(model_asset_path=MP_MODEL_PATH),\n",
    "    running_mode=VisionRunningMode.IMAGE,\n",
    "    num_faces=1,\n",
    "    output_face_blendshapes=True,\n",
    "    output_facial_transformation_matrixes=True,\n",
    ")\n",
    "\n",
    "def get_face_crop(bgr_img, target_size=IMG_SIZE):\n",
    "    \"\"\"Detect face via MediaPipe and return (cropped_rgb, lmks_norm, bbox).\"\"\"\n",
    "    rgb = cv2.cvtColor(bgr_img, cv2.COLOR_BGR2RGB)\n",
    "    mp_img = mp.Image(image_format=mp.ImageFormat.SRGB, data=rgb)\n",
    "    with FaceLandmarker.create_from_options(mp_opts) as fl:\n",
    "        result = fl.detect(mp_img)\n",
    "    if not result.face_landmarks:\n",
    "        return None, None, None\n",
    "    lmks = np.array([[lm.x, lm.y] for lm in result.face_landmarks[0]])\n",
    "    H, W = rgb.shape[:2]\n",
    "    lmks_px = lmks * [W, H]\n",
    "    x1, y1 = lmks_px.min(axis=0)\n",
    "    x2, y2 = lmks_px.max(axis=0)\n",
    "    pad = 0.2 * max(x2 - x1, y2 - y1)\n",
    "    x1, y1 = max(0, int(x1 - pad)), max(0, int(y1 - pad))\n",
    "    x2, y2 = min(W, int(x2 + pad)), min(H, int(y2 + pad))\n",
    "    crop = cv2.resize(rgb[y1:y2, x1:x2], (target_size, target_size))\n",
    "    return crop, lmks, (x1, y1, x2, y2)\n",
    "\n",
    "def encode_image(rgb_crop):\n",
    "    \"\"\"Run SMIRK encoder on a 224x224 RGB crop; returns dict of FLAME params.\"\"\"\n",
    "    tensor = TRANSFORM(rgb_crop).unsqueeze(0).to(DEVICE)\n",
    "    with torch.no_grad():\n",
    "        out = encoder(tensor)\n",
    "    return out\n",
    "\n",
    "def compute_nme(flame_lmks2d, mp_lmks_px):\n",
    "    \"\"\"NME with inter-ocular normalisation (indices 33 and 263 are outer eye corners).\"\"\"\n",
    "    pred = flame_lmks2d[0].cpu().numpy()  # (68, 2)\n",
    "    # Scale mp landmarks to 224\n",
    "    gt = mp_lmks2d = mp_lmks_px[:68]  # use first 68 mediapipe lmks as pseudo-GT\n",
    "    # Normalise mp to [0,224] range using bbox of gt\n",
    "    d_io = np.linalg.norm(gt[45] - gt[36]) + 1e-8  # outer eye corners (approx)\n",
    "    nme = np.mean(np.linalg.norm(pred - gt, axis=1)) / d_io\n",
    "    return float(nme)\n",
    "\n",
    "print('Helper functions defined.')\n",
])

